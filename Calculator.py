def addition(a, b):
  return a + b

def subtraction(a, b):
  return a - b

def multiplication(a, b):
  return a * b

def division(a, b):
  return a / b

def exp(a, b):
  return a ** b

print(addition(5, 5))
print(subtraction(4, 1))
print(multiplication(3, 3))
print(division(10, 5))
print(exp(2, 2))